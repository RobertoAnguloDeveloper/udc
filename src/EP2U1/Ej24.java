    private void calcularBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_calcularBtnActionPerformed
        int contadorServicio1 = 0, contadorServicio2 = 0, contadorServicio3 = 0
                , contadorServicio4 = 0, contadorManana = 0, contadorTarde = 0;
        
        String servicioMasPrestado = "", jornadaMasUsada = "";
        
        for(int i =0; i<gasolineras.size(); i++){
            switch(gasolineras.get(i).getClaseServicio()){
                case 1:
                    contadorServicio1++;
                    break;
                    
                case 2:
                    contadorServicio2++;
                    break;
                    
                case 3:
                    contadorServicio3++;
                    break;
                    
                case 4:
                    contadorServicio4++;
                    break;
            }
        }
        
        if(contadorServicio1 > contadorServicio2 
                && contadorServicio1 > contadorServicio3 
                && contadorServicio1 > contadorServicio4){
            servicioMasPrestado = "Servicio 1";
            
            for(int i = 0; i<gasolineras.size(); i++){
                if(gasolineras.get(i).getClaseServicio() == 1){
                    if(gasolineras.get(i).getJornada().toLowerCase().equals("ma√±ana")){
                        contadorManana++;
                    }else{
                        contadorTarde++;
                    }
                }
            }
            
            if(contadorManana > contadorTarde){
                jornadaMasUsada = "Ma√±ana";
            }else{
                jornadaMasUsada = "Tarde";
            }
            
        }else if(contadorServicio2 > contadorServicio1 
                && contadorServicio2 > contadorServicio3 
                && contadorServicio2 > contadorServicio4){
            servicioMasPrestado = "Servicio 2";
            
            for(int i = 0; i<gasolineras.size(); i++){
                if(gasolineras.get(i).getClaseServicio() == 2){
                    if(gasolineras.get(i).getJornada().toLowerCase().equals("ma√±ana")){
                        contadorManana++;
                    }else{
                        contadorTarde++;
                    }
                }
            }
            
            if(contadorManana > contadorTarde){
                jornadaMasUsada = "Ma√±ana";
            }else{
                jornadaMasUsada = "Tarde";
            }
        }else if(contadorServicio3 > contadorServicio1 
                && contadorServicio3 > contadorServicio2 
                && contadorServicio3 > contadorServicio4){
            servicioMasPrestado = "Servicio 3";
            
            for(int i = 0; i<gasolineras.size(); i++){
                if(gasolineras.get(i).getClaseServicio() == 3){
                    if(gasolineras.get(i).getJornada().toLowerCase().equals("ma√±ana")){
                        contadorManana++;
                    }else{
                        contadorTarde++;
                    }
                }
            }
            
            if(contadorManana > contadorTarde){
                jornadaMasUsada = "Ma√±ana";
            }else{
                jornadaMasUsada = "Tarde";
            }
        }else if(contadorServicio4 > contadorServicio1 
                && contadorServicio4 > contadorServicio2 
                && contadorServicio4 > contadorServicio3){
            servicioMasPrestado = "Servicio 4";
            
            for(int i = 0; i<gasolineras.size(); i++){
                if(gasolineras.get(i).getClaseServicio() == 4){
                    if(gasolineras.get(i).getJornada().toLowerCase().equals("ma√±ana")){
                        contadorManana++;
                    }else{
                        contadorTarde++;
                    }
                }
            }
            
            if(contadorManana > contadorTarde){
                jornadaMasUsada = "Ma√±ana";
            }else{
                jornadaMasUsada = "Tarde";
            }
        }
        
        //IMPRIME EL VALOR DE CADA SERVICIO
        //IMPRIME CANTIDAD DE VECES DE CADA SERVICIO
        for(int i = 0; i<gasolineras.size(); i++){
            switch(gasolineras.get(i).getClaseServicio()){
                case 1:
                    resultadoTextArea.append("Valor servicio 1: $"
                            +gasolineras.get(i).getValorServicio()
                            +"\nCantidad: "+contadorServicio1+"\n");
                    break;
                    
                case 2:
                    resultadoTextArea.append("Valor servicio 2: $"
                            +gasolineras.get(i).getValorServicio()
                            +"\nCantidad: "+contadorServicio2+"\n");
                    break;
                    
                case 3:
                    resultadoTextArea.append("Valor servicio 3: $"
                            +gasolineras.get(i).getValorServicio()
                            +"\nCantidad: "+contadorServicio3+"\n");
                    break;
                    
                case 4:
                    resultadoTextArea.append("Valor servicio 4: $"
                            +gasolineras.get(i).getValorServicio()
                            +"\nCantidad: "+contadorServicio4+"\n");
                    break;
            }
        }
        
        resultadoTextArea.append("************************************"
                + "\nEl servicio m√°s prestado fue el *"
                +servicioMasPrestado.toLowerCase()+"*"+
                "\nEn la jornada de la *"+jornadaMasUsada.toLowerCase()+"*");
    }//GEN-LAST:event_calcularBtnActionPerformed

    private void valorServicioTextFieldKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_valorServicioTextFieldKeyTyped
        validador.validaNumerosDouble(evt);
        if(valorServicioTextField.getText().length() > 8){
            Toolkit.getDefaultToolkit().beep();
            evt.consume();
        }            
    }//GEN-LAST:event_valorServicioTextFieldKeyTyped

    private void agregarBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_agregarBtnActionPerformed
        if(valorServicioTextField.getText().isEmpty()){
            valorServicioTextField.setText("$0");
            JOptionPane.showMessageDialog(null, "POR FAVOR LLENE LOS CAMPOS"
                    ,"ADVERTENCIA",JOptionPane.ERROR_MESSAGE);
        }else{
            int claseServicio = Integer.valueOf(claseServicioComboBox.getSelectedItem().toString());
            double valorServicio = Double.valueOf(valorServicioTextField.getText());
            gasolinera = new Gasolinera(claseServicio
                , jornadaComboBox.getSelectedItem().toString().toLowerCase()
                , valorServicio);
            gasolineras.add(gasolinera);
            calcularBtn.setEnabled(true);
        }
    }//GEN-LAST:event_agregarBtnActionPerformed

